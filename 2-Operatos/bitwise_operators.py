x=10
y=8
print(bin(x))
print(bin(y))

# AND (&)
print("x & y = ",x&y)

# OR (|)
print("x | y = ",x|y)

# XOR(^)
print("x ^ y = ",x^y)

# NOT(~)
print("~x =", ~x)  # ~10 = -(1010+1) => -(1011) => -(11) formula : {~x = -x-1}

# left shift(<<) = > (add zero's at the end)

print("x << 2 = ",x<<2) # x=10 => 1010 00=(40)

# right shift(>>)

print("x >> 2 = ",x>>2) #  add zero in starting
print("x >> 3 = ",x>>3) # 


