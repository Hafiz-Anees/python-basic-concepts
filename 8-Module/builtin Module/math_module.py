import math

# ceil ==> reurn integer value only 

num = 10.3

print(math.ceil(num))

# fabs ==> return absolute and positive value

negtive_num = -10.3
print(math.fabs(negtive_num))

# factorial  ==> value must be positive

print(math.factorial(5))

# floor ==> return the back value  if it is not an integer

print(math.floor(10.7))

# fsum ==> return the sum of all value of a list or tuple.

print(math.fsum([1,2,3,4,5])) 

# sqrt ==> return the sqaure  root of any number

print(math.sqrt(81))

# pow ==> return the value of base raised to the power of exponent

print(math.pow(5,2))

